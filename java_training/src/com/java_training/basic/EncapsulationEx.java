public class EncapsulationEx {
	private String name;
	private int id;
	private int age;
	//alt+shift+s===for generate setter and getter
	private String getName() {
		return name;
	}
	private void setName(String name) {
		this.name = name;
	}
	private int getId() {
		return id;
	}
	private void setId(int id) {
		this.id = id;
	}
	private int getAge() {
		return age;
	}
	private void setAge(int age) {
		this.age = age;
	}

	//this, this(),super, super(), final , static, access specifier
}
